
<p class="descrip">The goal of this site to make the City of South Bend's "South Bend Businesses Open to All Customers" directory more accessible</p>

<p>This was created as the final project for CS50, an intro to Computer Science course available through edx.org
from Harvard University.</p>
<p>According to South Bend's website, <strong>"These businesses are committed to a welcoming environment for everyone regardless of 
race, religion or sexual orientation. These businesses registered voluntarily, please be respectful."</strong></p>
<p>All data, and the description are from <a href="https://data.southbendin.gov/Business/South-Bend-Businesses-Open-to-All-Customers/mqet-2q6r">South Bend's database</a></a> </p>
